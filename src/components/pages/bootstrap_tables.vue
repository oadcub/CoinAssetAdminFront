                    var html = [];
                    $.each(row, function(key, value) {
                        if (key.substr(0, 1) != "_") {
                            html.push('<p><b>' + key + ':</b> ' + value + '</p>');
                        }
                    });
                    return html.join('');
                }
            });

        });
